package esercizio_19;


/**
 * The type Word games.
 */
public class WordGames  {

    /**
     * Add hello word string.
     *
     * @param word the word
     * @return the string
     */
    public String addHelloWord(String word) {
        return "Hello " + word;
    }

    /**
     * Gets full name.
     *
     * @param name    the name
     * @param surname the surname
     * @return the full name
     */
    public String getFullName(String name, String surname) {
        return name + " " + surname;
    }
}
